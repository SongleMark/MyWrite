/*
1,从键盘输入三种商品的价格，输出三种商品的平均价格

2,输入一个小写字符，输出该小写字符对应的大写字符

3,从键盘任意输入一个整数，输出该整数的个位数

4,从键盘输入两个数，分别作  + - ×  / %运算，并输出结果

5,从键盘 输入一个五位数，分别输出个十百千万位上的数字

   */


#include <stdio.h>


int main()
{
		int num;
		int g,s,b,q,w;
		printf("请输入一个五位数：");
		scanf("%d",&num);
        g = num%10;
		s = num/10%10;
		b = num/100%10;
		q = num/1000%10;
		w = num/10000;

		printf("g=%d,s=%d,b=%d,q=%d,w=%d\n",g,s,b,q,w);

		return 0;
}
